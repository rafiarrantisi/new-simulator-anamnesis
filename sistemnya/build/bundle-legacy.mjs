// ============================================================
// OphthaSim — Legacy bundler
// ------------------------------------------------------------
// Concatenates the original .js / .jsx files (UNCHANGED, byte-for-byte)
// in the EXACT order the original HTML loaded them, into a single
// ES-module entry (src/main.jsx) that Vite can build for production.
//
// Why concatenate instead of refactor to imports:
//   The original app runs every file in ONE shared global scope
//   (Babel-standalone + classic <script> tags). All 191 top-level
//   identifiers are unique (verified), so a single shared module
//   scope reproduces the exact same runtime behavior — zero changes
//   to design, content, or component logic.
// ============================================================

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { dirname, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');

// Exact load order from "Virtual Patient Simulator.html"
const LOAD_ORDER = [
  'data.js',
  'cases-extra.js',
  'case-tracks.js',
  'exam-data.js',
  'tutorial-scripts.js',
  'osce-scoring.js',
  // Abstraksi seam (Fase 1) — harus dimuat sesudah data.js (detectCategory,
  // CASES, loadProfile) dan sebelum simulator.jsx yang memanggilnya.
  'engine/patient-engine.js',
  'engine/data-store.js',
  'engine/case-adapter.js',
  'engine/evaluation-bridge.js',
  'engine/voice-engine.js',
  'components.jsx',
  'onboarding.jsx',
  'screens.jsx',
  'simulator.jsx',
  'exam-instruments.jsx',
  'exam-instruments-2.jsx',
  'exam-slitlamp.jsx',
  'exam-fundus.jsx',
  'stations/station-shell.jsx',
  'stations/station-va-v3.jsx',
  'stations/station-pupil-v3.jsx',
  'stations/station-tonometry-v3.jsx',
  'stations/station-slitlamp-v3.jsx',
  'stations/station-fundus-v3.jsx',
  'ocular-exam.jsx',
  'case-builder.jsx',
  'auth-screens.jsx',
  'profile-features.jsx',
  'learning-extras.jsx',
];

const HTML_FILE = 'Virtual Patient Simulator.html';

function extractInlineApp(html) {
  // The src-loaded scripts are `<script type="text/babel" src="...">`.
  // The single inline App script is `<script type="text/babel">` (no src),
  // so this pattern matches ONLY the inline block.
  const m = html.match(/<script type="text\/babel">\n?([\s\S]*?)<\/script>/);
  if (!m) throw new Error('Could not locate the inline App <script> block in ' + HTML_FILE);
  return m[1];
}

const header = `// AUTO-GENERATED by build/bundle-legacy.mjs — DO NOT EDIT THIS FILE.
// Edit the original .js / .jsx source files instead; this is just a build
// artifact that re-creates the app's single shared scope for Vite.
import React from 'react';
import * as ReactDOM from 'react-dom/client';
import confetti from 'canvas-confetti';
import '../design.css';

// The original code calls window.confetti(...) directly.
window.confetti = confetti;

// C3 (kontrak v0.7.0): base URL backend dari Vite env. Kosong → mode
// offline/dummy (StaticPatientEngine + CASES dummy). Di-set → RAG backend.
window.OPHTHA_API_BASE = import.meta.env.VITE_API_BASE || '';
`;

const parts = [header];

for (const rel of LOAD_ORDER) {
  const abs = join(ROOT, rel);
  if (!existsSync(abs)) throw new Error('Missing source file: ' + rel);
  const code = readFileSync(abs, 'utf8');
  parts.push(`\n// ===== BEGIN ${rel} =====\n`);
  parts.push(code);
  parts.push(`\n// ===== END ${rel} =====\n`);
}

const html = readFileSync(join(ROOT, HTML_FILE), 'utf8');
parts.push(`\n// ===== BEGIN inline App (from ${HTML_FILE}) =====\n`);
parts.push(extractInlineApp(html));
parts.push(`\n// ===== END inline App =====\n`);

const outDir = join(ROOT, 'src');
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });
const outFile = join(outDir, 'main.jsx');
writeFileSync(outFile, parts.join(''), 'utf8');

console.log(`[bundle-legacy] Wrote ${outFile} from ${LOAD_ORDER.length} source files + inline App.`);
